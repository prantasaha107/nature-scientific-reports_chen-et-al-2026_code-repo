#Verifies protein names against the HGNC
# Returns : List of Standardized HGNC gene names
# Returns : Fuzzy Suggestions
def verify_proteins_using_HGNC(protein_list):
    exact_matches = []
    fuzzy_suggestions = {}

    for protein in protein_list:
        # Strict length filter we exclude anything 3 chars or less
        if len(protein) <= 3:
            print(f"Skipping '{protein}' — too short.")
            continue
        try:
            hgnc_id = hgnc_client.get_hgnc_id(protein)
            if hgnc_id:
                standard_name = hgnc_client.get_hgnc_name(hgnc_id)
                if standard_name:
                    exact_matches.append(standard_name)
            else:
                # Did not find the protein now attempt fuzzy suggestion
                all_hgnc_names = [
                    hgnc_client.get_hgnc_name(hid)
                    for hid in hgnc_client.hgnc_names.keys()
                    if hgnc_client.get_hgnc_name(hid)
                ]
                close_matches = get_close_matches(protein, all_hgnc_names, n=3, cutoff=0.8)
                if close_matches:
                    fuzzy_suggestions[protein] = close_matches
                    print(f"[HGNC] '{protein}' not verified. Suggestions: {close_matches}")
                else:
                    print(f"[HGNC] '{protein}' not found. No suggestions.")
        except Exception as e:
            print(f"[HGNC] Error processing '{protein}': {e}")

    return list(set(exact_matches)), fuzzy_suggestions



# Verify the extracted text using UniProt
# send batched REST API requests
# Returns : List of Standardized protein names that matched UniProt.
# Returns : Fuzzy Suggestions
def verify_proteins_uniprot_version_1(protein_list, reviewed=True, batch_size=50, similarity_threshold=0.8):
    url = "https://rest.uniprot.org/uniprotkb/search"
    exact_matches = []
    fuzzy_suggestions = {}

    for i in range(0, len(protein_list), batch_size):
        batch = [p for p in protein_list[i:i + batch_size] if len(p) > 3]  # length filter

        if not batch:
            continue

        query = " OR ".join(f'gene:"{protein}"' for protein in batch)
        params = {
            'query': query,
            'fields': 'accession,id,protein_name,gene_names',
            'format': 'json'
        }
        if reviewed:
            params['reviewed'] = 'true'

        try:
            response = requests.get(url, params=params)

            if response.status_code == 200:
                data = response.json().get('results', [])

                # Collect all gene names from entire batch response for fuzzy pool
                all_returned_gene_names = []
                exact_matched_proteins = set()

                for entry in data:
                    gene_names = []
                    for gene in entry.get('genes', []):
                        gene_name = gene.get('geneName', {})
                        if isinstance(gene_name, dict):
                            gene_names.append(gene_name.get('value', ''))
                            synonyms = gene.get('synonyms', [])
                            if isinstance(synonyms, list):
                                gene_names.extend([syn.get('value', '') for syn in synonyms if isinstance(syn, dict)])

                    gene_names = [name.strip().upper() for name in gene_names if name]
                    all_returned_gene_names.extend(gene_names)

                    for protein in batch:
                        if protein.upper() in gene_names:
                            exact_matches.append(protein)
                            exact_matched_proteins.add(protein)

                # Fuzzy matching only for proteins that didn't get exact match
                fuzzy_pool = list(set(all_returned_gene_names))
                for protein in batch:
                    if protein not in exact_matched_proteins:
                        close_matches = difflib.get_close_matches(
                            protein.upper(),
                            fuzzy_pool,
                            n=3,
                            cutoff=similarity_threshold
                        )
                        if close_matches:
                            fuzzy_suggestions[protein] = close_matches
                            print(f"[UniProt] '{protein}' not verified. Suggestions: {close_matches}")
                        else:
                            print(f"[UniProt] '{protein}' not found. No suggestions.")
            else:
                print(f"Error {response.status_code} for batch starting with '{batch[0]}': {response.text}")

        except Exception as e:
            print(f"Request failed for batch starting with '{batch[0]}': {e}")

    return list(set(exact_matches)), fuzzy_suggestions

# Manages Protein Verification across UniProt and HGNC.
# Combines and deduplicates exact matches from both databases
# Combines fuzzy suggestions from both datatsets.
# retruns : Deduplicated list of strictly verified protein names from either or both databases.
# retruns: Suggested Correct Names for Manual Review.

def protein_processor(protein_list):
  # Since protein names are usually capital lettered.
    protein_list_upper = [item.upper() for item in protein_list]

    print("Verifying against UniProt")
    uniprot_exact, uniprot_fuzzy = verify_proteins_uniprot_version_1(protein_list_upper, reviewed=True)

    print("*****")

    print("Verifying against HGNC")
    hgnc_exact, hgnc_fuzzy = verify_proteins_using_HGNC(protein_list_upper)

    # List 1: Strict verified — union of exact matches from both databases
    final_verified = list(set(uniprot_exact + hgnc_exact))

    # List 2: Fuzzy suggestions which is an union of fuzzy matches from both databases
    # If a protein appears in both, merge their suggestions
    all_fuzzy = {}
    for protein, suggestions in {**uniprot_fuzzy, **hgnc_fuzzy}.items():
        if protein in final_verified:
            continue  # already exactly verified, skip
        if protein in all_fuzzy:
            all_fuzzy[protein] = list(set(all_fuzzy[protein] + suggestions))
        else:
            all_fuzzy[protein] = suggestions

    print(f"\n RESULTS ")
    print(f"UniProt exact matches : {len(uniprot_exact)}")
    print(f"HGNC exact matches    : {len(hgnc_exact)}")
    print(f"Final verified list   : {len(final_verified)}")
    print(f"Flagged for manual review : {len(all_fuzzy)}")

    print(f"\n--- Final Verified Proteins ---")
    for p in final_verified:
        print(f"  {p}")

    print(f"\n--- Flagged for Manual Review ---")
    for protein, suggestions in all_fuzzy.items():
        print(f"  {protein} -> possible matches: {suggestions}")

    return final_verified, all_fuzzy



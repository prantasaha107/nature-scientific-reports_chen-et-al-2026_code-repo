#Removes page numbers from extracted PDF text.
def remove_page_numbers(text):
    return re.sub(r'\n\d+\n', '\n', text)

#Rejoins words that were split across lines with a hyphen.
def fix_hyphenated_words(text):
    return re.sub(r'(\w+)-\n(\w+)', r'\1\2', text)

# Break multiple whitespace characters into a single space.
def normalize_whitespace(text):
    return re.sub(r'\s+', ' ', text).strip()


#Corrects common OCR misrecognition errors such as O for 0 and I for 1.
def correct_ocr_errors(text):
    corrections = {
        '0': 'O',
        '1': 'I',
    }
    for wrong, correct in corrections.items():
        text = text.replace(wrong, correct)
    return text

#Strips non-alphanumeric and non-whitespace characters from text.
def remove_special_characters(text):
    return re.sub(r'[^\w\s]', '', text)

# Remove email address from text
def remove_email_addresses(text):
    email_regex = r'[\w\.-]+@[\w\.-]+\.\w+'
    return re.sub(email_regex, '', text)

#Removes four digit years and all caps section headings from text such as - RESULTS, DISCUSSIONS, INTRODUCTIONS , citation years
def remove_years_and_section_titles(text):
    text = re.sub(r'\d{4}', '', text)
    text = re.sub(r'[A-Z][A-Z0-9\s]+:', '', text)
    text = re.sub(r'\d+\.\s+[A-Z][A-Z0-9\s]+', '', text)
    text = re.sub(r'[A-Z][A-Z0-9\s]+', '', text)
    return text

#a pipeline of cleaning steps to raw PDF-extracted text.
def preprocess_text(ocr_text):

    # cleaned_text = remove_years_and_section_titles(ocr_text)
    # cleaned_text = remove_email_addresses(cleaned_text)

    cleaned_text = remove_page_numbers(ocr_text)
    # cleaned_text = fix_hyphenated_words(cleaned_text)
    cleaned_text = normalize_whitespace(cleaned_text)
    # cleaned_text = correct_ocr_errors(cleaned_text)
    cleaned_text = remove_special_characters(cleaned_text)
    return cleaned_text

#emoves stray punctuation characters from a list of string tokens
def clean_list(some_list):
    cleaned_list = []
    for item in some_list:
        item = re.sub(r"[',]", '', item).strip()
        cleaned_list.append(item)
    return cleaned_list



